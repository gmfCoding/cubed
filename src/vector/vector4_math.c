#include "vector4.h"

t_vec4	v4muls(t_vec4 f, t_vecd s)
{
	t_vec4	vec;

	vec.x = f.x * s;
	vec.y = f.y * s;
	vec.z = f.z * s;
	vec.w = f.w * s;
	return (vec);
}
